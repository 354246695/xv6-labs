	/* YOUR CODE HERE */
	// 3. 模仿内核swtch.S 添加
	// 当前寄存器保存在当前线程对应的contex对象（thread_switch函数 参数一）中
	sd ra, 0(a0)
    sd sp, 8(a0)
    sd s0, 16(a0)
    sd s1, 24(a0)
    sd s2, 32(a0)
    sd s3, 40(a0)
    sd s4, 48(a0)
    sd s5, 56(a0)
    sd s6, 64(a0)
    sd s7, 72(a0)
    sd s8, 80(a0)
    sd s9, 88(a0)
